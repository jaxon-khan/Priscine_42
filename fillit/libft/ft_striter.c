#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t	i;
	size_t	l;

	i = 0;
	if (!s || !(*f))
		return ;
	l = ft_strlen(s);
	while (i < l)
		f(&s[i++]);
}
